#键盘输入1个整型数、1个实数
a = int(input("请输入一个整型数："))
b = float(input("请输入一个实数："))

#输入的实数用四舍五入转换成整数
c = round(b)

#分别计算输入整型数和实数的三次方、开平方

d = a ** 3
e = a ** 0.5

f = b ** 3
g = b ** 0.5

#将第3步的计算结果用保留2位小数输出。

print(f"{a} 的三次方为 {d:.2f}")
print(f"{a} 开平方为 {e:.2f}")
print(f"{b} 的三次方为 {f:.2f}")
print(f"{b} 开平方为 {g:.2f}")